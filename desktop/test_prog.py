"""
1. PyAutoGUI
Описание: PyAutoGUI — это библиотека для автоматизации интерфейсов GUI, которая работает через эмуляцию клавиш и
кликов мыши. Она не требует интеграции с конкретным приложением и может работать с любым программным интерфейсом.

Возможности:
Нажатие клавиш и клики мыши.
Захват и анализ скриншотов.
Перемещение мыши, ввод текста.
Недостатки: PyAutoGUI работает на основе визуальных элементов, что делает его менее надежным при изменении
интерфейса приложения.
"""

import pyautogui

# Открытие приложения (например, блокнот)
pyautogui.hotkey('win', 'r')
pyautogui.write('notepad')
pyautogui.press('enter')

# Ввод текста
pyautogui.write('Hello, this is automated!')


"""
2. pywinauto
Описание: Pywinauto — это библиотека для автоматизации Windows GUI приложений через API Windows. 
Она позволяет более точно взаимодействовать с элементами интерфейса на уровне окна.

Возможности:
Поиск элементов окна (кнопки, поля ввода и т. д.).
Управление окнами и их элементами напрямую.
Получение информации о состоянии элементов интерфейса.
Поддерживаемые платформы: Только Windows.
"""

from pywinauto import Application

# Открытие приложения
app = Application().start('notepad.exe')

# Ввод текста в блокнот
app.UntitledNotepad.Edit.type_keys("Hello from pywinauto!", with_spaces=True)


